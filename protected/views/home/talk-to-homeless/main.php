                <h4>Our message is clear. </h4>
                <p>The homeless are real people, with real lives, real feelings, and real hopes and dreams. They also have real needs. Support doesn't need to be in the form of mittens, dollar bills or food. It could be simply listening to their stories and connecting with them person-to-person. Social connection, after all, is a basic human need.</p>
                <br>
                <h4>It starts with a "hello"</h4>

                <p>The next time you pass someone on the street asking you for spare change, take a moment to talk with them. You can give them change or share any snacks you may have, but also be sure to just say hello and speak with them for a while. Taking the time to talk to someone goes a long way. </p>

                <p>If you take the same route to work or school each morning then it is possible you will see the same homeless person on your commute more than once in a week. Say hello and get to know them. Or perhaps when you buy your morning coffee or breakfast snack, by an extra cup or muffin and offer it to your new friend. </p>
                <br>

                <h4>Keep it simple</h4>
                <p>Some people may not feel comfortable speaking up. That's alright. Simply make eye contact and say hello with a smile. The homeless often feel invisible. Acknowledge them and let them know you care.</p>
                <br>
                <h4>A little conversation can go a long way</h4>
                <p>Creating a connection with a homeless person helps build the strength and courage they need to move forward in their life. When the homeless feel cared about, they are able to care about themselves and have courage to move forward in their lives. Not only will they feel encouraged, it will help you feel good too!</p>



