            <hr id="what-is-the-hp-website"/>
            <h4>What is the HomelessPartners website?</h4>
            <p>On our website, you will find the personal stories of many homeless people from several cities.</p>
            <p>The homeless are real people, with real lives, real feelings, and real hopes and dreams. By clicking on a city you can read their stories and choose one or more people to send a personal, caring gift or message by delivering it to the local shelter.</p>
            <p id="how-does-it-work">Giving money directly to them may not be the most helpful, but a caring gift or message goes a long way when they realize that someone who knows only their brief story cares enough to let them know. When the homeless feel cared about, they are able to care about themselves and have courage to move forward in their lives. Not only will they feel encouraged, it will help you feel good too!</p>

            <hr/>
            <h4>How does it work?</h4>
            <p>Just go to HomelessPartners.com and follow the steps.</p>
            <ol>
                <li>You choose a city and shelter near you,</li>
                <li>Then read the stories of people who stay at shelters.</li>
                <li>Then pledge a gift for as many people as you choose.</li>
                <li>Then you get the gift(s) and deliver them to the shelter with a caring note for each person.</li>
            </ol>
            <p>Voila! Harry has the new blue coat he asked for, in his size, with a caring note so he knows that someone cares about him.</p>
            <p id="who-are-the-homeless">It’s all about connection – helping the homeless know that someone in the community cares.</p>

            <hr />
            <h4>Who are the homeless?</h4>
            <p id="what-do-homeless-people-need">They are men and women who have lost their homes and families. They are brothers and sisters, sons and daughters, fathers and mothers, aunts and uncles, nieces and nephews.
                If they were your family members, regardless of their circumstances or choices, you would want them to know you care. We believe everyone is our family member.
                Each one has a real life story with real reasons for their homelessness. Sometimes they are recently homeless.
                Sometimes they have lived on and off the street and in shelters for many years.
                Some are the working poor who don't earn enough to afford rent and rely on temporary shelters and agencies to make ends meet.
                Many of the homeless suffer from mental illness. This can lead to drug addiction as they try to help themselves feel better and cope with their homeless condition.
                Although they sometimes look able-bodied, mental illness is not always outwardly apparent.
                It prevents people from holding down a job as much as a physical handicap prevents a wheelchair-bound person from walking. No one deserves to suffer as the homeless do.
                In desperation, they often go hungry and sleep in the cold and rain. Sometimes they are able to find a bed or meal in a shelter but on the street, they are often beaten, robbed, diseased, and dying.</p>

            <hr/>
            <h4>What do homeless people need?</h4>
            <p id="how-add-new-person-to-list">The homeless do not have extravagant needs. One of the most pervasive problems they face, especially in winter, is hypothermia. They need winter coats and footwear, thermal underwear, warm dry socks, gloves, toques, and scarves. Some who are capable of working, especially in the building trades, are not able to work because they have no tools, tool belt or steel-toe work boots. These can be donated new or used as long as they are in sturdy and reusable condition. Many homeless people are talented artists, musicians, and writers. By gifting art supplies like canvasses, acrylic paints, brushes, etc. they can be afforded the tools to create works of art.</p>

            <hr/>
            <h4>If someone knows of a person who should be on the list, how can they apply?</h4>
            <p id="when-will-the-stories-be-uploaded">The people on the lists are created from those who come to local shelters. If you know someone who should be on the list, advise them to call one of the participating shelters and find out when the interviews are taking place. This usually happens between the last week of November and the first or second week of December.</p>

            <hr/>
            <h4>When will the stories be uploaded?</h4>
            <p id="how-do-I-know-if-the-person-received-the-gift">The stories are typically added to <a href="http://www.homelesspartners.com/">HomelessPartners.com</a> closer to Christmas as those who share their stories at the corresponding shelters are a transient population and are in and out of the shelters for a short period of time. It will be hard to keep track of the recipient and story if it remains on the website for an extended period of time, so the pledge option will be removed.</p>

            <hr/>
            <h4>How do I know if the person received the gift?</h4>
            <p>You can call the shelter and ask them about a specific person.</p>
            <p>This is an aspect we are trying hard to improve. We would like to offer the gift recipients an option to write a thank you note and we are trying to figure out a way to gather the thank you notes so that they can be searched by the name, gift and ID #.</p>
            <p id="can-I-buy-the-gifts-online">It’s a challenge we're currently trying to address.</p>

            <hr/>
            <h4>Can I buy the gifts online?</h4>
            <p>You can purchase gifts at any retail location. You can also purchase gifts online through any online vendor and have them delivered to the shelter. There is usually an option to include a personal note as well. </p>
            <p id="can-I-make-a-donation-directly-to-hp">Unfortunately, we do not have an option to order them online through our website but we hope to add that feature in the future.</p>

            <hr/>
            <h4>Can I make a donation directly to HomelessPartners?</h4>
            <p>Yes, for the first time, HomelessPartners is accepting donations. These funds will be used for: accounting and taxation fees, a part-time employee position, software expenses and will be made available to participating city coordinators who will oversee the purchase of gifts for those listed on the site. </p>
            <p id="made-a-pledge-on-the-website">All funds will be open and transparent to anyone.</p>

            <hr/>
            <h4>I've made a pledge on the website, but when I revisit the site, the stories are still unpledged? What do I do?</h4>
            <p id="cant-purchase-or-deliver-gift">Many people experience this issue when they have not confirmed their pledges within the shopping cart in the upper right corner of the screen. Make sure you complete the process by checking out from the shopping cart above. You should receive an email confirmation detailing your donations.</p>

            <hr/>
            <h4>What if I find out I can’t purchase or deliver my gift?</h4>
            <p id="is-it-possible-to-meet-person">If, for any reason -and we understand that things happen beyond your control- you are not able to deliver your gift, please go to your HomelessPartners account as soon as possible and “unpledge” your gift so that someone else might be able to make the pledge instead.</p>

            <hr/>
            <h4>Is it possible to meet the person who we bought the gift for?</h4>
            <p id="deliver-gifts-after-the-holidays">The shelter staff are the best people to make this determination. If you call and ask about the specific person, they can advise you about the possibility of making this happen.</p>

            <hr/>
            <h4>Can I deliver gifts after the holidays?</h4>
            <p>You can always make donations to the shelter anytime, but because the homeless population is quite transient, it’s possible the intended recipient may not be there.</p>
            <p id="purchase-the-items-requested">However, when you take your gift, make sure to include a label on the outside with a description of the gift (size, male or female, type of gift). The shelter coordinator will give the gift to another appropriate person if the intended recipient is not there.</p>

            <hr/>
            <h4>Can I purchase the items requested or is there a shopper to do this?</h4>
            <p>Most times, there are no shoppers that can purchase the specific gift you request. The primary intention of HomelessPartners is to foster connection and personal caring.</p>
            <p id="donate-to-the-program-anyone-hasnt-received">However, sometimes at the end of the season, donations are used to purchase gifts for people who are still unpledged, but the gift selections are made by the shoppers. For example,  it’s not possible to ask for the shopper to purchase a blue coat for Harry.</p>

            <hr/>
            <h4>Can I donate to the program so that, after all of the pledges are made, if anyone hasn't received their request, the money I send can be used to help them?</h4>
            <p id="is-this-only-for-homeless">Yes, in most cities, we have a buyer who will carefully examine the list to try to ensure that everyone on the list gets something.</p>

            <hr/>
            <h4>The title says “Homeless Partners”– is this only for the homeless?</h4>
            <p id="hp-organized">Yes, the people on this list are either completely homeless and frequent shelters of are living in a shelter.</p>

            <hr/>
            <h4>How is HomelessPartners organized?</h4>
            <p>Since 2005, HomelessPartners has been operated entirely by volunteers. Different organizations (mostly churches, but sometimes other groups) coordinate volunteers in their city to go into shelters in late November or early December and personally interview the shelter residents, asking them a little about their life – whatever they feel comfortable sharing – as well as items for their Christmas wish list. These volunteers then post the interview and the wish list on the website: HomelessPartners.com.</p>
            <p>Other volunteers may send out a press release to the media (radio, TV and newspaper) and post about HomelessPartners on social media (facebook, twitter and emails) so that people in the community find out about the website, read the stories, buy the gifts and deliver them to the shelters.</p>
            <p id="hp-funded">The result: the shelter residents receive a gift, just for them, and they know that someone cares about them as an individual.</p>

            <hr/>
            <h4>How is HomelessPartners organized?</h4>
            <p>Since 2005, HomelessPartners has not been an official organization – only groups of people in different cities who want to help the homeless. None of the volunteers have been salaried and they have not received any donations as an overall organization. Sometimes individuals have made donations through the specific city organization that is coordinating the effort and sometimes people make donations directly to shelters. </p>
            <p>This year, HomelessPartners is taking a major growth step. It has become federally incorporated as a non-profit organization with a view toward becoming a registered charity.</p>
            <p>In October-November of 2013, the first fundraising campaign was undertaken through indiegogo and we successfully raised $10,000. These funds will go towards expenses for volunteer programmers to create a new website – their food and resource expenses, as well as accounting costs and registration fees. </p>
            <p>Also, funds will be forwarded to each participating city so that a “buyer” in that city can purchase individual gifts for people who are listed on the site who may have been ungifted. One of the purposes is to ensure that each person on the list receives something.</p>
            <p id="how-to-involve">All funds collected with be absolutely, transparently, available to anyone.</p>

